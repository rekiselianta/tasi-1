        <h2>FAQ Tugas Akhir</h2><br>

<p><h5><strong>Apa saja persyaratan dan ketentuan pelaksanaan seminar tugas akhir?</strong></h5></p>
<ol>
<li>Seminar Tugas Akhir harus dihadiri minimal 5 orang mahasiswa MG.</li>
<li>Telah mendapatkan persetujuan oleh dosen pembimbing.</li>
<li>Telah mendaftarkan diri untuk melakukan seminar ke Tata Usaha Prodi minimal 3 hari sebelum hari-H Seminar.</li>
<li>Undangan seminar yang ditandatangani dosen pembimbing telah diedarkan minimal 3 hari sebelum hari-H Seminar.</li>
<li>Mahasiswa mendistribusikan ringkasan (abstrak) tugas akhir 1 halaman yang sudah diperiksa oleh dosen pembimbing kepada seluruh peserta seminar.</li>
<li>Mahasiswa mempersiapkan perlengkapan presentasi TA yang meliputi laptop, LCD projector, pointer.</li>
<li>Materi presentasi mencakup latar belakang, tujuan dan metodologi penelitian, percobaan dan hasil-hasilnya, pembahasan, dan kesimpulan.</li>
<li>Mahasiswa berpakaian rapi dengan lengan panjang berdasi dan bercelana kain serta hadir sebelum waktu seminar dimulai untuk mempersiapkan seminar.</li>
<li>Mahasiswa melakukan presentasi TA dengan menggunakan bahasa Indonesia yang baik dan benar.</li>
<li>Mahasiswa menjawab pertanyaan-pertanyaan yang diajukan oleh dosen pembimbing dan mahasiswa yang hadir.</li>
<li>Mencatat perbaikan dan masukan-masukan dari dosen pembimbing dan mahasiswa yang hadir.</li>
<li>Waktu seminar <strong>maksimal 50 menit</strong> (mulai pukul 07.00 sd pukul 18.00) dengan perincian waktu sebagai berikut:
<ul>
<li>Pembukaan : <strong><em>5</em></strong><strong><em> menit</em></strong><em>.</em></li>
<li>Dosen pembimbing membuka dan memperkenalkan mahasiswa yang bersangkutan (nama, NIM, judul TA dan pengantar ringkas penelitian yang dilakukan bila diperlukan).</li>
<li>Presentasi tugas akhir: <strong><em>25</em></strong><strong><em> menit</em></strong> menggunakan <em>overhead projector.</em></li>
<li>Tanya – Jawab: <strong><em>2</em></strong><strong><em>0</em></strong><strong><em> menit</em></strong><em>.</em></li>
<li>Pertanyaan diajukan oleh mahasiswa yang hadir dan dosen pembimbing.</li>
<li>Dosen pembimbing dan mahasiswa lain dapat memberikan saran-saran untuk perbaikan Tugas Akhir yang dilakukan.</li>
<li>Penutup: <strong><em>2</em></strong><strong><em> menit</em></strong><em>.</em></li>
<li>Dosen pembimbing menyampaikan catatan-catatan penting dari pelaksanaan seminar yang baru saja dilakukan.</li>
<li>Dosen pembimbing menutup seminar.</li>
</ul>
</li>
</ol>
<p><h5><strong>Apa saja persyaratan dan ketentuan mahasiswa dapat mengikuti sidang sarjana?</strong></h5></p>
<ol>
<li>Mahasiswa sudah lulus semua matakuliah diluar TA dengan jumlah SKS minimal 138 SKS termasuk matakuliah MG 3090 Kerja Praktek.</li>
<li>Mahasiswa sudah melakukan seminar Tugas Akhir dan dinyatakan dapat mengikuti sidang sarjana oleh dosen pembimbingnya.</li>
<li>Mahasiswa telah menghadiri seminar tugas akhir mahasiswa S1 MG lainnya minimal 10x.</li>
<li>Mahasiswa telah membuat paper yang berisi ringkasan tugas akhirnya untuk dapat diajukan pada jurnal (nasional maupun internasional), seminar (nasional maupun internasional) dan repository. Paper dibuat dalam format 2 kolom.</li>
<li>Mahasiswa telah memenuhi persyaratan-persyaratan administrasi seperti mengisi biodata, menyerahkan foto, mengisi form pembimbingan TA yang diserahkan kepada Tata Usaha.</li>
<li>Draft Tugas Akhir sudah harus diterima oleh semua dosen penguji maksimal 2 hari sebelum hari sidang sarjana. Bila hari sidang sarjana dilakukan pada Hari Senin, draft tugas akhir harus sudah diterima dosen penguji maksimal Jum’at sore pukul 16.00, kecuali dosen penguji memberikan kesempatan untuk disampaikan ke rumah sebelum Sabtu pukul 12.00.</li>
<li>Sebelum sidang dimulai, mahasiswa diharuskan mempresentasikan ringkasan tugas akhirnya dengan slide presentasi atau secara lisan dengan waktu maksimal 10 menit.</li>
<li>Mahasiswa berpakaian rapi dengan lengan panjang berdasi dan bercelana kain.</li>
</ol>
<p>&nbsp;</p>
<p><h5><strong>Bagaimana tata cara sidang sarjana?</strong></h5></p>
<p>Waktu sidang <strong>maksimal 120 menit</strong> (mulai pukul 07.00 sd pukul 18.00) dengan perincian waktu sebagai berikut:</p>
<ol>
<li>Pembukaan : <strong><em>5</em></strong><strong><em> menit</em></strong><em>.</em></li>
<li>Ketua sidang membuka dan memperkenalkan mahasiswa yang diuji (nama, NIM, judul TA, IP sebelum sidang, tempat KP dan dosen pembimbing TA-nya).</li>
<li>Ketua sidang mempersilahkan dosen pembimbing memberikan pengantar mengenai penelitian tugas akhir yang dilakukan oleh mahasiswa yang bersangkutan.</li>
<li>Presentasi ringkasan tugas akhir: <strong><em>10</em></strong><strong><em> menit</em></strong> dapat menggunakan <em>overhead projector</em> atau secara lisan.</li>
<li>Tanya – Jawab: <strong><em>90</em></strong><strong><em> menit</em></strong> dalam 2 sesi.</li>
<li>Pertanyaan pertama diajukan oleh Penguji ke-2 diikuti oleh Penguji ke-3 (ketua sidang) dan penguji ke-1 (pembimbing).</li>
<li>Pertanyaan pada sesi pertama lebih difokuskan pada materi penelitian tugas akhirnya.</li>
<li>Pertanyaan pada sesi kedua melanjutkan pertanyaan pada sesi pertama dan pertanyaan-pertanyaan umum mengenai bidang ilmu Teknik Metalurgi yang terutama berkaitan dengan topik penelitian yang dilakukan.</li>
<li>Penutup: <strong><em>15</em></strong><strong><em> menit</em></strong><em>.</em></li>
<li>Mahasiswa yang diuji dipersilahkan menunggu hasil ujian di luar ruangan sidang sarjana.</li>
<li>Penguji dan pembimbing memberikan nilai sidang. Pembimbing memberikan nilai Tugas Akhir. Nilai sidang dan nilai Draft Tugas Akhir mempunyai bobot yang sama dalam menentukan nilai akhir Mata Kuliah Tugas Akhir.</li>
<li>Penguji memberikan saran-saran terkait perbaikan Tugas Akhir dan saran kepada mahasiswa yang bersangkutan.</li>
<li>Penguji berhak memberikan saran mengenai nilai Draft Tugas Akhir dan mendiskusikan nilai akhir bersama-sama dengan dosen pembimbing.</li>
<li>Ketua sidang mengumumkan nilai Tugas Akhir, menyampaikan saran-saran perbaikan Tugas Akhir, saran-saran kepada mahasiswa yang bersangkutan dan menutup sidang sarjana.</li>
<li>Ketua sidang, penguji dan pembimbing menandatangani berita acara sidang.</li>
</ol>
<p>&nbsp;</p>
<p><h5><strong>apa saja Ketentuan draft tugas akhir?</strong></h5></p>
<ol>
<li>Jumlah halaman (dari Bab Pendahuluan sampai dengan Daftar Pustaka) dibatasi <strong>50 halaman.</strong> Data-data percobaan yang banyak jumlahnya dapat dimasukkan dalam lampiran.</li>
<li>Template tugas Akhir dapat diunduh di website Prodi MG: <a href="http://www.metallurgy.itb.ac.id">metallurgy.itb.ac.id.</a></li>
<li>Tujuan percobaan harus terjawab pada kesimpulan.</li>
<li>Metodologi <u>bukan</u> merupakan diagram alir atau prosedur percobaan, melainkan alur pikir penelitian secara menyeluruh.</li>
<li>Pustaka ditulis dalam bentuk angka dalam kurung kurawal superscript. Dalam Daftar Pustaka nomor pustaka diurutkan berdasarkan urutan pustaka tersebut diacu dalam teks.</li>
<li>Nomor pustaka juga harus dituliskan dalam gambar dan tabel yang dikutip.</li>
<li>Nomor pustaka <u>tidak</u> dituliskan pada Daftar Isi, Daftar Gambar, Daftar Tabel dan Bab atau Subbab.</li>
<li>Singkatan yang digunakan harus diuraikan kepanjangannya terlebih dahulu pada saat pertama kali digunakan. Contoh: Analisa komposisi kimia sampel bijih dilakukan dengan <em>X-Ray Fluoroscence</em> (XRF) dan <em>Atomic Absorption Spectrophotometer</em> (AAS). Selanjutnya bisa dipakai singkatannya saja.</li>
<li>Setiap akan menyajikan gambar dan tabel, harus ada kalimat yang mengaju kepada tabel atau gambar tersebut sebelumnya.</li>
<li>Tabel tidak boleh terpotong dan diusahakan tersaji dalam satu halaman.</li>
<li>Judul gambar dan tabel tidak boleh terpisah dari gambar dan tabelnya.</li>
<li>Berikut ini format umum laporan Tugas Akhir (dapat juga dilihat di template)
<ul>
<li>Kertas : HVS A4 berat 80 g/m<sup>2</sup> (HVS 80 GSM).</li>
<li>Margin Kiri, Kanan, Atas, Bawah : 4 cm, 3 cm, 3 cm, 3 cm.</li>
<li>Spasi : 1,5.</li>
<li>Font judul : Times new roman, 14, Bold.</li>
<li>Font sub judul 1 (contoh 1.1) : Times new roman, 12, Bold.</li>
<li>Font sub judul 2 (contoh 1.1.1) : Times new roman, 12.</li>
<li>Font teks : Times new roman, 12.</li>
</ul>
</li>
<li>Jarak antar paragraf : 2x 1,5 spasi.</li>
</ol>
